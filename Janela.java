    private void cboPaisMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_cboPaisMouseReleased
                    int idade;
            if (!(cboPais.getSelectedItem().toString().equals("Selecione..."))&&
                    (cboIdade.getSelectedItem().toString().equals("Selecione..."))){
                idade=Integer.parseInt(cboIdade.getSelectedItem().toString());
                if(idade<=18){
                    cliente[i].setLimitecredito(100);
                    txtLimite.setText("R$ 100,00");
                }
                else if (idade<=35){
                    cliente[i].setLimitecredito(300);
                    txtLimite.setText("R$ 300,00");
                }
                else if (idade>35){
                    cliente[i].setLimitecredito(500);
                    txtLimite.setText("R$ 500,00");
                }
                if(cliente[i].getPais().toUpperCase().equals("BRASIL")){
                    cliente[i].setLimitecredito(cliente[i].getLimitecredito() + 100);
                }
                txtLimite.setText(cliente[i].getLimitecredito()+"");
            }
    }//GEN-LAST:event_cboPaisMouseReleased

