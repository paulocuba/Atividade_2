    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        int a=0;
        int b=1;
        
        if ((txtNome.getText().equals(""))||(txtTelefone.getText().equals(""))||
                (txtLimite.getText().equals(""))||(cboPais.getSelectedItem().toString().toUpperCase().equals("SELECIONE..."))
                ||(txtSigla.getText().equals(""))){
            JOptionPane.showMessageDialog(null,"Por Favor, preecha todos os campos!");
        }
        else{
            do{
                String nome=txtNome.getText();
                cliente[i]=new Cliente();
                String comparacao;
 
                b=1;
             
                cliente[i].setNome(txtNome.getText().toUpperCase());
                cliente[i].setTelefone(txtTelefone.getText().toUpperCase());
                cliente[i].setPais(cboPais.getSelectedItem().toString().toUpperCase());
                cliente[i].setSigla(txtSigla.getText().toUpperCase());
                cliente[i].setLimitecredito(Double.parseDouble(txtLimite.getText()));

                a=JOptionPane.showConfirmDialog(null,"As informações estão corretas?",
                        "Mensagem",JOptionPane.YES_NO_OPTION);
                for (int j=0;j<i;j++){
                    comparacao=cliente[j].getNome();
                    if(cliente[i].getNome().equals(comparacao)){
                        b=0;
                        JOptionPane.showMessageDialog(null,"Não é possível realizar a inscrição de dois clientes com o mesmo nome!");
                    }
                }
                if ((nome.length())<5){
                      b=0;
                      JOptionPane.showMessageDialog(null,"Não é possível inserir um cliente com nome inferior a 5 dígitos!");
                }
            }while((a==JOptionPane.NO_OPTION)||(b==0));
            JOptionPane.showMessageDialog(null,"Cadastro realizado com sucesso!");
            i=i+1;
            inicio();
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

